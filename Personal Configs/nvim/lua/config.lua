-- Configuracion basica
vim.cmd("set expandtab") -- Converts tabs to spaces
vim.cmd("set tabstop=4") -- Number of spaces that a <Tab> in the file counts for
vim.cmd("set softtabstop=4") -- Number of spaces that a <Tab> counts for while performing editing operations
vim.cmd("set shiftwidth=4") -- Number of spaces to use for each step of (auto)indent
vim.cmd("set number") -- Show line numbers

vim.g.mapleader = " " -- Sets <leader> key to space

vim.opt.background = "dark" -- set this to dark or light
